function createDepth(buyData, sellData) {
    const option = {
        series: []
    }
    if (buyData.length > 0) {
        option.series[0] = {
            name: 'buy',
            data: buyData
        }
    }
    if (sellData.length > 0) {
        option.series[1] = {
            name: 'sell',
            data: sellData
        }
    }
    depthChart.setOption(option)
}

